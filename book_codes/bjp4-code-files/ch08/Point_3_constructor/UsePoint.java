public class UsePoint {
    public static void main(String[] args) {
        // create two Point objects
        Point p1 = new Point(5, -2);
        Point p2 = new Point(-4, 3);

        // print each point
        System.out.println("(" + p1.x + ", " + p1.y + ")");
        System.out.println("(" + p2.x + ", " + p2.y + ")");
    }
}